package com.zjp.designpattern.builder.improve;

public class House {
    private String basise;
    private String wall;
    private String roofed;

    public String getBasise() {
        return basise;
    }

    public void setBasise(String basise) {
        this.basise = basise;
    }

    public String getWall() {
        return wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }

    public String getRoofed() {
        return roofed;
    }

    public void setRoofed(String roofed) {
        this.roofed = roofed;
    }
}
